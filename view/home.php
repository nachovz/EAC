		<h2>Web 2.0 y los discapacitados</h2>
		<p>Podemos afirmar que la mayor&iacute;a de los sitios o aplicaciones web 2.0 generan una barrera para los usuarios con discapacidad. Esto es debido a que el concepto original de estas herramientas fueron dise&ntilde;ados sin prestar atenci&oacute;n a la accesibilidad.</p>
		<p> Algunos problemas que los usuarios con discapacidad pueden enfrentar:</p>
		<ul>
			<li>Falta de soporte de audio para acciones claves en el sistema (inicio de sesi&oacute;n, preguntas de seguridad).</li>
			<li>Interfaces inaccesibles por ser elementos <i>drag and drop</i> y carecen de una versi&oacute;n alternativa de uso con el teclado.</li>
			<li>No implementar una forma de notificaci&oacute;n auditiva para cambios din&aacute;micos -ejemplo Ajax-, para usuarios que utilizan dispositivos de lectura de pantalla.</li>
			<li>Contenido inaccesible creado por usuarios:
				<ul>
					<li>Im&aacute;genes sin texto alternativo.</li>
					<li>Contenido creado sin la utilizaci&oacute;n de c&oacute;digo sem&aacute;ntico.</li>
					<li>Dise&ntilde;os y estilos que presentan dificultad para leer.</li>
					<li>Contenido multimedia sin subt&iacute;tulos o contenido alternativo.</li>
				</ul>
			</li>
			<li>Falta de soporte para usuarios generadores de contenido sobre accesibilidad y su correcto uso.</li>
		</ul>
	</div>
	<div class="span5 offset2">
		<h2>Intentemos una web 2.0 accesible</h2>
		<p>Si queremos desarrollar una aplicaci&oacute;n web 2.0 con soporte para personas con discapacidad podemos utilizar los l&iacute;neamientos basados en experiencias de usuario y realizado por expertos.</p>
		<p>Existen dos compendios de &eacute;stos l&iacute;neamientos producidos por la <a href="http://www.w3.org/">W3C</a> que apoyan al desarrollo y las buenas pr&aacute;cticas de accesibilidad:<br /> <a href="http://www.w3.org/TR/WAI-AUTOOLS/">ATAG</a> (Authoring Tool Accessibility Guidelines) <br /> <a href="http://www.w3.org/TR/WCAG/">WCAG</a> (Web Content Accessibility Guidelines)</p>
		<p>Algunos l&iacute;neamientos clave del ATAG:</p>
		<ul>
			<li>Soporte para practicas de creaci&oacute;n de contenido accesible.</li>
			<li>Permitir herramientas para revisar y corregir contenido no accesible.</li>
			<li>Integrar soluciones accesibles en la interfaz de creaci&oacute;n del sistema.</li>
			
		</ul>
